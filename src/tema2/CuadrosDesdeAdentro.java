    @Override
    public void paint(Graphics g) {
        super.paint(g);
        g.setColor(Color.white);
        try {
            recursion(g, separacion);
        } catch (InterruptedException ex) {
            
        }
    }
    
    public void recursion(Graphics g, int separacion) throws InterruptedException {
        if (separacion > 50) {
            dibujar(g, separacion);
            Thread.sleep(pausa);
            recursion(g, separacion - 10);
        }
    }
    
    public void dibujar(Graphics g, int separacion) {
        ancho = getWidth();//para que el dibujo se
        alto = getHeight();//ajuste a la ventana
        
        xI = separacion; yI = separacion;
        xF = ancho - separacion; yF = yI;
        
        g.drawLine(xI, yI, xF, yF);
        
        xI = xF; yI = yF;
        xF = xI; yF = alto - separacion;
        
        g.drawLine(xI, yI, xF, yF);
        
        xI = xF; yI = yF;
        xF = separacion; yF = yI;
        
        g.drawLine(xI, yI, xF, yF);
        
        xI = xF; yI = yF;
        xF = xI; yF = separacion;
        
        g.drawLine(xI, yI, xF, yF);
    }
