    public void paint(Graphics pintar) {
        super.paint(pintar); // Generated from nbfs://nbhost/SystemFileSystem/Templates/Classes/Code/OverriddenMethodBody
        int ancho, alto;
        ancho = this.getWidth();
        alto = this.getHeight();
        int mitadX, mitadY;
        mitadX = ancho/2;
        mitadY=alto/2;
        
        pintar.drawLine(mitadX-50, mitadY, mitadX-100, mitadY);
        pintar.drawLine(mitadX+50, mitadY, mitadX+100, mitadY);
        
        pintar.drawLine(mitadX-50, mitadY, mitadX-50, mitadY+180);
        pintar.drawLine(mitadX+50, mitadY, mitadX+50, mitadY+180);
        
        pintar.drawLine(mitadX-50, mitadY+180, mitadX+50, mitadY+180);
        
        pintar.drawString("", alto, alto);
        }
